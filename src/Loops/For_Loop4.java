package Loops;

public class For_Loop4 {

	public static void main(String[] args) {
		
// WAP to print 5 to 1 number using for loop
		
//	starting condition -  5
//	end condition - 1
//  decrement =   --
		
//		              5>=1  - True
//		              1>=1  - True
//		              0>=1  - False
		for(int i=5 ; i>=1 ;i--) {
			
			System.out.println(i); //5,4,3,2,1
		}
        System.out.println("Hi");
	}

}
