package JavaBasics;

public class LocalVariable {

	public static void main(String[] args) {
// Variable are data containers used to store data values.
// Steps:1. Variable declaration
//		 2. Variable initialization 
//		 3. Usage 
// Variable declared inside method body is the local variable.
		int deposit = 3000;
		char section = 'A';
		String candidate = "Tejashree Telrandhe";
		double rate = 8.5;
		
		System.out.println("Initial deposit = "+deposit);
		System.out.println("Account section = "+section);
		System.out.println("Candidate name = "+candidate);
		System.out.println("Interest rate = "+rate);
	}

}
