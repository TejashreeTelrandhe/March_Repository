package ControlStatements;

public class if_else_statement3 {

	public static void main(String[] args) {

		int a = 10;
		int b = 20;
		int c = a + b; // 30

		// 10 +20 <25 -false
		if (c < 25) {
			System.out.println("a+b is less than 25");
		} else
			System.out.println("a+b is greater than 25");

	}

}
