package Operator;

public class ArithmeticOperator {

	public static void main(String[] args) {

	//	ArithmeticOperator  ---> + ,- ,*,/ ,%
				
		int a = 23 ;
		int b = 7 ;
		
		System.out.println(a+b);   // 30
		System.out.println(a-b);   //10
		System.out.println(a*b);   //200
		System.out.println(a/b);   //2
		System.out.println(a%b);   //0
	}

}
