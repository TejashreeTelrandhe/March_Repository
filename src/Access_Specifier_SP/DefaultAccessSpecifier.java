package Access_Specifier_SP;

public class DefaultAccessSpecifier {

	public static void main(String[] args) {
		DefaultAccessSpecifier P4 = new DefaultAccessSpecifier();
		P4.method10();
		P4.method11();
		P4.method12();
	}
	void method10() {
		System.out.println("default Method10");
	}

	void method11() {
		System.out.println("default Method11");
	}

	void method12() {
		System.out.println("default Method12");
	}
}
